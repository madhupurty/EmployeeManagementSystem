package com.ems.entity;

/**
 * =============================================================================
 * LEAVE TYPE ENUM - Types of Leave Available
 * =============================================================================
 */
public enum LeaveType {
    
    /**
     * Annual/Vacation leave - paid time off
     */
    ANNUAL,
    
    /**
     * Sick leave - for illness
     */
    SICK,
    
    /**
     * Casual leave - personal matters
     */
    CASUAL,
    
    /**
     * Maternity leave - for new mothers
     */
    MATERNITY,
    
    /**
     * Paternity leave - for new fathers
     */
    PATERNITY,
    
    /**
     * Bereavement leave - death in family
     */
    BEREAVEMENT,
    
    /**
     * Unpaid leave - leave without pay
     */
    UNPAID,
    
    /**
     * Compensatory off - for extra work
     */
    COMP_OFF,
    
    /**
     * Work from home
     */
    WORK_FROM_HOME
}
